public class dataTypes {
    public static void main(String[] args)
    {
        /// Data Types in Java
        int age = 12;       ////// Integer Data Type
        String userName= "De Developer";       ////// String Data Type
        boolean isMale = true;       ////// Boolean Data Type
        char value = 'B';       ////// Char Data Type
        double doubleValue = 21.2311d;       ////// Double Data Type
        float floatValue = 21.2311f;       ////// Float Data Type

        System.out.println(age);
        System.out.println(userName);
        System.out.println(isMale);
        System.out.println(value);
        System.out.println(doubleValue);
        System.out.println(floatValue);
    }
}
