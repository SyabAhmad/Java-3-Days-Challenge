public class FUncitons2 {
    public static int addition(int a, int b)
    {
        return a+b;
    }
    public static void main(String[]args)
    {
        /// functions returning Values with arguments
        int a = 12;
        int b = 21;
        int sum = addition(a,b);
        System.out.println(sum);
        /// OR
        System.out.println(addition(32,23));
    }
}
