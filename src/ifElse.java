public class ifElse {
    public static void main(String[]args)
    {
        //// if Else Statement in Java
        /// as these are Conditional Statement
        /// we Need condition to use if and else statement
        int a=10;
        int b=20;
        /// in brackets we write conditions
        // / and when it comes true the if statement will executes
        /// otherwise else statement will be executed
        if(a>b)
        {
            System.out.println("a is greater than b");
        }
        else
        {
            System.out.println("a is not greater than b");
        }
    }
}
